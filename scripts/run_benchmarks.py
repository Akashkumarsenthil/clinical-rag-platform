#!/usr/bin/env python3
"""Run chunking strategy benchmarks and save results to docs/chunking_benchmarks.md."""

from __future__ import annotations

import sys
from pathlib import Path

import structlog

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from src.evaluation.benchmark_runner import BenchmarkRunner

log = structlog.get_logger(__name__)

OUTPUT_PATH = Path(__file__).resolve().parent.parent / "docs" / "chunking_benchmarks.md"


def main() -> None:
    """Run all chunking strategy benchmarks and write the markdown report."""
    log.info("benchmark_runner_start")

    # Optionally pass sample doc paths via command-line arguments
    sample_docs: list[str] = sys.argv[1:] if len(sys.argv) > 1 else []

    runner = BenchmarkRunner(
        sample_doc_paths=sample_docs or None,
        num_eval_pairs=25,
    )
    report = runner.run_all_strategies()

    # Print to stdout
    print("\n" + "=" * 80)
    print("CHUNKING STRATEGY BENCHMARK RESULTS")
    print("=" * 80)
    print(report.markdown_table)
    print("=" * 80)

    for result in report.results:
        e = result.eval_results
        print(f"\nStrategy: {result.strategy}")
        print(f"  Chunks created   : {result.chunks_created}")
        print(f"  Faithfulness     : {e.faithfulness:.4f}")
        print(f"  Answer relevancy : {e.answer_relevancy:.4f}")
        print(f"  Context precision: {e.context_precision:.4f}")
        print(f"  Context recall   : {e.context_recall:.4f}")
        print(f"  Mean score       : {e.mean_score:.4f}")

    # Write to docs/chunking_benchmarks.md
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    header = (
        "# Chunking Strategy Benchmark Results\n\n"
        "Results generated by `scripts/run_benchmarks.py` using RAGAS metrics "
        "on 25 QA pairs per strategy.\n\n"
    )
    OUTPUT_PATH.write_text(header + report.markdown_table + "\n")
    log.info("benchmark_report_saved", path=str(OUTPUT_PATH))
    print(f"\nReport saved to: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
